import type React from "react";
import type { ThumbnailStyle } from "../assets/assets"
import { CpuIcon, ImageIcon, PenToolIcon, SquareIcon, SparkleIcon, ChevronDownIcon } from "lucide-react";


const StyleSelector = ({value, onChange, isOpen, setIsOpen} : {value: ThumbnailStyle; onChange: (style: ThumbnailStyle) => void; isOpen: boolean; setIsOpen: (isOpen: boolean) => void}) => {

    const styleDescriptions: Record<ThumbnailStyle, string> = {
        'Bold & Graphic': 'High contrast, bold typography, striking visuals.',
        'Minimalist': 'Clean, simple, lots of white space.',
        'Photorealistic': 'Photo-based, natural looking.',
        'Illustrated': 'Hand-drawn, artistic, creative.',
        'Tech/Futuristic': 'Sleek, modern, tech-inspired.',
    }

    const styleIcons: Record<ThumbnailStyle, React.ReactNode> = {
        'Bold & Graphic': <SparkleIcon className="h-4 w-4"/>,
        'Minimalist': <SquareIcon className="h-4 w-4"/>,
        'Photorealistic': <ImageIcon className="h-4 w-4"/>,
        'Illustrated': <PenToolIcon className="h-4 w-4"/>,
        'Tech/Futuristic': <CpuIcon className="h-4 w-4"/>,
    }

  return (
    <div className="relative space-y-3 dark">
        <label className="block text-sm font-medium text-zinc-200">Thumbnail Style</label>

        <button
        type="button"
        onClick={()=>setIsOpen(!isOpen)}
        className="flex w-full items-center justify-between rounded-md border px-4 py-3 text-left transition bg-white/8 border-white/10 text-zinc-200 hover:bg-white/12">
            <div className="space-y-1">
                <div className="flex items-center gap-2 font-medium">
                    {styleIcons[value]}
                    <span>{value}</span>
                </div>
                <p className="text-xs text-zinc-400">{styleDescriptions[value]}</p>
            </div>
            <ChevronDownIcon className= {['h-5 w-5 text-zinc-400 transition-transform', isOpen && 'rotate-180'].join(' ')} />
        </button>

        {isOpen && (
            <div className="absolute top-full left-0 right-0 mt-2 rounded-md border border-white/10 bg-zinc-950/95 shadow-lg z-50">
                {(Object.keys(styleDescriptions) as ThumbnailStyle[]).map((style) => (
                    <button
                        key={style}
                        type="button"
                        onClick={() => {
                            onChange(style);
                            setIsOpen(false);
                        }}
                        className={`w-full px-4 py-3 text-left transition border-b border-white/5 last:border-b-0 hover:bg-white/10 ${
                            value === style ? 'bg-white/15' : ''
                        }`}
                    >
                        <div className="flex items-center gap-2 font-medium text-zinc-200">
                            {styleIcons[style]}
                            <span>{style}</span>
                        </div>
                        <p className="text-xs text-zinc-400 mt-1">{styleDescriptions[style]}</p>
                    </button>
                ))}
            </div>
        )}
    </div>
  )
}

export default StyleSelector